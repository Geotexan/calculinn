function calcula() {
	//Inputs
	var e = $('#i-e').val();
	var ih = $('#i-h').val();
	var iy = $('#i-y').val();
	var id = $('#i-d').val();
	if (ih.length === 0 && iy.length === 0 || e === 0 || id === 0 ) {
		alert("Debe introducir todos los valores del formulario");
		return false;
	}
	
	//Storage interval from the input
	var h;
	var d;
	var y;

	if (ih <= 20) {
		h = '<20';
	} else if (ih > 20 ) {
		h = ">20";
	}

	if (iy <1) {
		y = '<1';
	} else if (iy >=1 && iy < 1.5 ) {
		y = "1-1.5";
	} else if (iy >= 1.5 ) {
		y = ">1.5";
	}

	if (id < 0) {
		alert ("El valor de D debe ser mayor que 0");
		return false;
	} else if (id >= 0 && id < 60) {
		d = "0-60";
	} else if (id >= 60 && id < 120) {
		d = "60-120";
	} else if (id >= 120) {
		d = '>120'
	}

	$.each(vertederos, function(i, item) {
		if (vertederos[i]['Espesor'] == e) {
      if (vertederos[i]['Altura'] == h ) {
				if (vertederos[i]['Densidad'] == y) {
					if (vertederos[i]['Diametro'] == d) {
						//Relleno valores del formulario y salgo de la iteraci√≥n
						$('#rt-geotexan-value').val(vertederos[i]['Geotexan']['RT']);
						$('#elong-geotexan-value').val(vertederos[i]['Geotexan']['ELONG']);
						$('#cbr-geotexan-value').val(vertederos[i]['Geotexan']['CBR']);
						$('#perfcono-geotexan-value').val(vertederos[i]['Geotexan']['PERF_CONO']);
						$('#kplano-geotexan-value').val(vertederos[i]['Geotexan']['KPLANO']);
						$('#abcono-geotexan-value').val(vertederos[i]['Geotexan']['AB_PORO']);
						$('#rt-geotextil-value').val(vertederos[i]['Geotextil']['RT']);
						$('#elong-geotextil-value').val(vertederos[i]['Geotextil']['ELONG']);
						$('#cbr-geotextil-value').val(vertederos[i]['Geotextil']['CBR']);
						$('#perfcono-geotextil-value').val(vertederos[i]['Geotextil']['PERF_CONO']);
						$('#kplano-geotextil-value').val(vertederos[i]['Geotextil']['KPLANO']);
						$('#abcono-geotextil-value').val(vertederos[i]['Geotextil']['AB_PORO']);
						$('#product').val(vertederos[i]['GEOTESAN']);
						return false;
					}
				}
			}
		}
	});	
}
