function calcula() {
	//Inputs
	var ih = $('#i-h').val();
	var e = $('#i-e').val();
	var id = $('#i-d').val();
	if (ih.length === 0 || id === 0 ) {
		alert("Debe introducir todos los valores del formulario");
		return false;
	}
	
  if (isNaN(id) || isNaN(ih)) {
    alert("Los valores deben ser numéricos");
    return false;
  }
	//Storage interval from the input
	var h;
	var d;

	if (ih < 0) {
    alert("La altura debe ser manor que 0");
    return false;
  } else if (ih < 10) {
		h = '[0..10)'
	} else if (ih >= 10 ) {
		h = "[10..∞)";
	}

	if (id < 0) {
		alert ("El valor de D debe ser mayor que 0");
		return false;
	} else if (id >= 0 && id < 60) {
		d = "[0..60)";
	} else if (id >= 60 && id < 120) {
		d = "[60..120)";
	} else if (id >= 120) {
		d = '[120..∞)'
	}

  var result = false;
	$.each(balsas_gtx, function(i, item) {
		if (balsas_gtx[i]['Altura'] == h ) {
			if (balsas_gtx[i]['Espesor'] == e) {
				if (balsas_gtx[i]['Dmax'] == d) {
					//Relleno valores del formulario y salgo de la iteración
					$('#rt-geotexan-value').val(balsas_gtx[i]['RT']);
					$('#elong-geotexan-value').val(balsas_gtx[i]['ELONG']);
					$('#cbr-geotexan-value').val(balsas_gtx[i]['RCBR']);
					$('#perfcono-geotexan-value').val(balsas_gtx[i]['RPERF_CONO']);
					$('#kplano-geotexan-value').val(balsas_gtx[i]['KVERT']);
					$('#abcono-geotexan-value').val(balsas_gtx[i]['AB_PORO']);
					$('#rt-geotextil-value').val(balsas_gtxtl[i]['RT']);
					$('#elong-geotextil-value').val(balsas_gtxtl[i]['ELONG']);
					$('#cbr-geotextil-value').val(balsas_gtxtl[i]['RCBR']);
					$('#perfcono-geotextil-value').val(balsas_gtxtl[i]['RPERF_CONO']);
					$('#kplano-geotextil-value').val(balsas_gtxtl[i]['KVERT']);
					$('#abcono-geotextil-value').val(balsas_gtxtl[i]['AB_PORO']);
					$('#product').val(balsas_gtxtl[i]['GEOTESAN']);
          result = true;
					return true;
				}
			}
		}
	});
  if (!result) {
      alert('No se han encontrado resultados');
  }
}