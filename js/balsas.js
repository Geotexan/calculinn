function calcula() {
	//Inputs
	var ih = $('#i-h').val();
	var e = $('#i-e').val();
	var id = $('#i-d').val();
	if (ih.length === 0 || e === 0 || id === 0 ) {
		alert("Debe introducir todos los valores del formulario");
		return false;
	}
	
	//Storage interval from the input
	var h;
	var d;

	if (ih <= 10) {
		h = '<10'
	} else if (ih > 10 ) {
		h = ">10";
	}

	if (id < 0) {
		alert ("El valor de D debe ser mayor que 0");
		return false;
	} else if (id >= 0 && id < 60) {
		d = "0-60";
	} else if (id >= 60 && id < 120) {
		d = "60-120";
	} else if (id >= 120) {
		d = '>120'
	}

	$.each(balsas, function(i, item) {
		if (balsas[i]['Altura'] == h ) {
			if (balsas[i]['Espesor'] == e) {
				if (balsas[i]['Diametro'] == d) {
					//Relleno valores del formulario y salgo de la iteraci√≥n
					$('#rt-geotexan-value').val(balsas[i]['Geotexan']['RT']);
					$('#elong-geotexan-value').val(balsas[i]['Geotexan']['ELONG']);
					$('#cbr-geotexan-value').val(balsas[i]['Geotexan']['CBR']);
					$('#perfcono-geotexan-value').val(balsas[i]['Geotexan']['PERF_CONO']);
					$('#kplano-geotexan-value').val(balsas[i]['Geotexan']['KPLANO']);
					$('#abcono-geotexan-value').val(balsas[i]['Geotexan']['AB_PORO']);
					$('#rt-geotextil-value').val(balsas[i]['Geotextil']['RT']);
					$('#elong-geotextil-value').val(balsas[i]['Geotextil']['ELONG']);
					$('#cbr-geotextil-value').val(balsas[i]['Geotextil']['CBR']);
					$('#perfcono-geotextil-value').val(balsas[i]['Geotextil']['PERF_CONO']);
					$('#kplano-geotextil-value').val(balsas[i]['Geotextil']['KPLANO']);
					$('#abcono-geotextil-value').val(balsas[i]['Geotextil']['AB_PORO']);
					$('#product').val(balsas[i]['GEOTESAN']);
					return true;
				}
			}
		}
	});
}