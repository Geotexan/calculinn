function calcula(){
  var espesor = $('#etongada').val();
  var arozamiento = $('#arozamiento').val();
  var amuro = $('#amuro').val();
  var imuro = $('#imuro').val();
  var drelleno = $('#drelleno').val();
  var sobrecarga = $('#sobrecarga').val();

  if (drelleno.length === 0) {
    alert("Debe introducir todos los valores del formulario");
    return false;
  }

  if (isNaN(drelleno)) {
    alert("Los valores deben ser numéricos");
  }
  
  if (drelleno < 15 && drelleno > 30) {
    alert("Densidad debe ser mayor que 15 y menor que 30");
    return false;
  }
  var densidad = '[15..30]';

  var result = false;
  $.each(muros_gtx, function(i, item) {
    if (muros_gtx[i]['Espesor'] == espesor){
     if(muros_gtx[i]['Angulo_Rozamiento'] == arozamiento) {
      if (muros_gtx[i]['Altura'] == amuro) {
        if(muros_gtx[i]['Inclinacion'] == imuro) {
          if (muros_gtx[i]['Sobrecarga'] == sobrecarga)
            if (muros_gtx[i]['Densidad'] == densidad) {
               //Relleno valores del formulario y salgo de la iteración
              $('#rt-geotexan-value').val(muros_gtx[i]['RT']);
              $('#elong-geotexan-value').val(muros_gtx[i]['ELONG']);
              $('#anclaje-geotexan-value').val(muros_gtx[i]['L_ANCLAJE']);
              $('#rt-geotextil-value').val(muros_geomalla[i]['RT']);
              $('#elong-geotextil-value').val(muros_geomalla[i]['ELONG']);
              $('#anclaje-geotextil-value').val(muros_geomalla[i]['L_ANCLAJE']);
              $('#product').val(muros_geomalla[i]['GEOTESAN']);
              result = true;
              return false;
            }
          }
        }
      }
    }
  });
  if (!result) {
    alert('No se han encontrado resultados');
  }
}