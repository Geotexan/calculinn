      $('#anclaje-geotexan-value').val(item['Geotexan']['ANCLAJE']);
      $('#rt-geotextil-value').val(item['Geotexan']['RT']);
      $('#elong-geotextil-value').val(item['Geotexan']['ELONG']);
      $('#anclaje-geotextil-value').val(item['Geotexan']['ANCLAJE']);
