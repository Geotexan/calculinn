function calcula() {
	//Inputs
	var icbr = $('#i-cbr').val();
	var ie = $('#i-e').val();
	var id = $('#i-d').val();
	if (icbr.length === 0 || ie === 0 || id === 0 ) {
		alert("Debe introducir todos los valores del formulario");
		return false;
	}
	
	//Storage interval from the input
	var cbr;
	var e;
	var d;

	if (icbr <= 0) {
		alert("CBR debe ser mayor que 0");
		return false;
	} else if (icbr > 0 ) {
		cbr = "g0";
		if (icbr >= 0 && icbr < 1) {
			cbr = "0-1";
		} else if (icbr >= 1 && icbr < 3) {
			cbr = "1-3";
		} else if (icbr>= 3 && icbr < 6) {
			cbr = "3-6";
		} else if (icbr >= 6) {
			cbr = "ge6";
		}
	}

	if (ie < 200) {
		alert("E debe ser mayor o igual a 200");
		return false;
	} else if (ie >=200 && ie < 400) {
		e = "ge200-l400";
	} else if (ie >= 400) {
		e = "ge400";
	}

	if (id < 150) {
		d = "l150";
	} else if (id >= 150 && id < 300) {
		d = "ge150-l300";
	} else if (id >= 300) {
		alert("D debe ser menor que 300");
		return false;
	}

	$.each(carreteras, function(i, item) {
		if (carreteras[i]['CBR'] == cbr ) {
			if (carreteras[i]['Espesor'] == e) {
				if (carreteras[i]['Dmax'] == d) {
					//Relleno valores del formulario y salgo de la iteraci√≥n
					$('#rt-geotexan-value').val(carreteras[i]['Geotexan']['RT']);
					$('#elong-geotexan-value').val(carreteras[i]['Geotexan']['ELONG']);
					$('#cbr-geotexan-value').val(carreteras[i]['Geotexan']['RCBR']);
					$('#perfcono-geotexan-value').val(carreteras[i]['Geotexan']['RPERF_CONO']);
					$('#abcono-geotexan-value').val(carreteras[i]['Geotexan']['AB_PORO']);
					$('#rt-geotextil-value').val(carreteras[i]['Geotextil']['RT']);
					$('#elong-geotextil-value').val(carreteras[i]['Geotextil']['ELONG']);
					$('#cbr-geotextil-value').val(carreteras[i]['Geotextil']['RCBR']);
					$('#perfcono-geotextil-value').val(carreteras[i]['Geotextil']['RPERF_CONO']);
					$('#abcono-geotextil-value').val(carreteras[i]['Geotextil']['AB_PORO']);
					$('#product').val(carreteras[i]['GEOTESAN']);
					return true;
				}
			}
		}
	});

}