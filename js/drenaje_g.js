function calcula() {
	var terreno = $('#terreno').val();
	var arido = $('#arido').val();
	var id = $('#i-d').val();
	if (id.length === 0) {
		alert("Debe introducir todos los valores del formulario");
		return false;
	}

	if (isNaN(id)) {
		alert("Los valores deben ser numéricos");
		return false;
	}

	if (id < 0) {
		alert('El valor de D debe ser mayor que 0');
		return false;
	} else if (id >= 0 && id < 60 ) {
		var d = '[0..60)';
	} else if (id >= 60 && id < 100) {
		var d = '[60..100)';
	} else {
		var d = '[100..∞)';
	}


	var result = false;
	$.each(drenajes_gtx, function(i, item) {
		if (drenajes_gtx[i]['Terreno'] === terreno ) {
			if (drenajes_gtx[i]['Arido'] === arido ) {
				if (drenajes_gtx[i]['Dmax'] == d ) {
					//Relleno valores del formulario y salgo de la iteración
					$('#rt-geotexan-value').val(drenajes_gtx[i]['RT']);
					$('#elong-geotexan-value').val(drenajes_gtx[i]['ELONG']);
					$('#cbr-geotexan-value').val(drenajes_gtx[i]['RCBR']);
					$('#rperf-geotexan-value').val(drenajes_gtx[i]['PERF_CONO']);
					$('#abertura-poro-geotexan-value').val(drenajes_gtx[i]['AB_PORO']);
					$('#kvert-geotexan-value').val(drenajes_gtx[i]['KVERT']);
					$('#rt-geotextil-value').val(drenajes_gtxtl[i]['RT']);
					$('#elong-geotextil-value').val(drenajes_gtxtl[i]['ELONG']);
					$('#cbr-geotextil-value').val(drenajes_gtxtl[i]['RCBR']);
					$('#rperf-geotextil-value').val(drenajes_gtxtl[i]['PERF_CONO']);
					$('#abertura-poro-geotextil-value').val(drenajes_gtxtl[i]['AB_PORO']);
					$('#kvert-geotextil-value').val(drenajes_gtxtl[i]['KVERT']);
					$('#product').val(drenajes_gtxtl[i]['GEOTESAN']);
					result = true;
					return false;
				}
			}
		}
	});
	if (!result) {
		alert('No se han encontrado resultados');
	}
}