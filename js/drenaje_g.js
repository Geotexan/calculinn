function calcula() {
	var terreno = $('#terreno').val();
	var arido = $('#arido').val();
	var id = $('#i-d').val();

	if (id < 0) {
		alert('El valor de D debe ser mayor que 0');
		return false;
	} else if (id >= 0 && id < 60 ) {
		var d = 'ge0-l60';
	} else if (id >= 60 && id < 100) {
		var d = 'ge60-l100';
	} else {
		var d = 'ge100';
	}


	$.each(drenajes, function(i, item) {
		if (drenajes[i]['Terreno'] === terreno ) {
			if (drenajes[i]['Arido'] === arido ) {
				if (drenajes[i]['Dmax'] == d ) {
					//Relleno valores del formulario y salgo de la iteraciÃ³n
					$('#rt-geotexan-value').val(drenajes[i]['Geotexan']['RT']);
					$('#elong-geotexan-value').val(drenajes[i]['Geotexan']['ELONG']);
					$('#cbr-geotexan-value').val(drenajes[i]['Geotexan']['RCBR']);
					$('#rperf-geotexan-value').val(drenajes[i]['Geotexan']['PERF_CONO']);
					$('#abertura-poro-geotexan-value').val(drenajes[i]['Geotexan']['AB_PORO']);
					$('#kvert-geotexan-value').val(drenajes[i]['Geotexan']['KVERT']);
					$('#rt-geotextil-value').val(drenajes[i]['Geotextil']['RT']);
					$('#elong-geotextil-value').val(drenajes[i]['Geotextil']['ELONG']);
					$('#cbr-geotextil-value').val(drenajes[i]['Geotextil']['RCBR']);
					$('#rperf-geotextil-value').val(drenajes[i]['Geotextil']['PERF_CONO']);
					$('#abertura-poro-geotextil-value').val(drenajes[i]['Geotextil']['AB_PORO']);
					$('#kvert-geotextil-value').val(drenajes[i]['Geotextil']['KVERT']);
					$('#product').val(drenajes[i]['GEOTESAN']);
					return false;
				}
			}
		}
	});
}